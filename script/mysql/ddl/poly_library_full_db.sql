DROP TABLE IF EXISTS LOAN;
DROP TABLE IF EXISTS BOOK;
DROP TABLE IF EXISTS USER_INFO;

SET FOREIGN_KEY_CHECKS = 1;

-- [2] USER_INFO 테이블 생성
CREATE TABLE USER_INFO (
    MEMBER_ID   VARCHAR(50)  NOT NULL COMMENT '회원 아이디',
    PASSWORD    VARCHAR(100) NOT NULL COMMENT '비밀번호',
    NAME        VARCHAR(50)  NOT NULL COMMENT '이름',
    ROLE        VARCHAR(20)  NOT NULL DEFAULT 'USER' COMMENT '권한(ADMIN/USER)',
    PHONE       VARCHAR(20)  DEFAULT NULL COMMENT '전화번호',
    EMAIL       VARCHAR(100) DEFAULT NULL COMMENT '이메일',
    PRIMARY KEY (MEMBER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='회원 정보';

-- [3] BOOK 테이블 생성
CREATE TABLE BOOK (
    BOOK_ID     INT          NOT NULL AUTO_INCREMENT COMMENT '도서번호',
    TITLE       VARCHAR(200) NOT NULL COMMENT '도서명',
    AUTHOR      VARCHAR(100) NOT NULL COMMENT '저자',
    PUBLISHER   VARCHAR(100) NOT NULL COMMENT '출판사',
    STATUS      CHAR(1)      NOT NULL DEFAULT 'Y' COMMENT '대출가능여부(Y/N)',
    CATEGORY    VARCHAR(50)  DEFAULT '기타' COMMENT '분류',
    PRIMARY KEY (BOOK_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='도서 정보';

-- [4] LOAN 테이블 생성
CREATE TABLE LOAN (
    LOAN_ID            INT          NOT NULL AUTO_INCREMENT COMMENT '대출번호',
    BOOK_ID            INT          NOT NULL COMMENT '도서번호',
    MEMBER_ID          VARCHAR(50)  NOT NULL COMMENT '회원아이디',
    LOAN_DATE          VARCHAR(20)  NOT NULL COMMENT '대출일(YYYY-MM-DD)',
    RETURN_DATE        VARCHAR(20)  DEFAULT NULL COMMENT '반납예정일',
    ACTUAL_RETURN_DATE VARCHAR(20)  DEFAULT NULL COMMENT '실제반납일',
    STATUS             VARCHAR(20)  NOT NULL DEFAULT 'ACTIVE' COMMENT '상태(ACTIVE/PENDING/RETURNED)',
    PRIMARY KEY (LOAN_ID),
    FOREIGN KEY (BOOK_ID)   REFERENCES BOOK(BOOK_ID),
    FOREIGN KEY (MEMBER_ID) REFERENCES USER_INFO(MEMBER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='도서 대출 이력';

